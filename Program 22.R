# creating vectors
vec1 <- c(1, 2, 3, 4, 5)
vec2 <- c(10, 20, 30)

# element-wise operations using recycling
add_result <- vec1 + vec2
sub_result <- vec1 - vec2
mul_result <- vec1 * vec2

# printing results
cat("Addition result:", add_result, "\n")
cat("Subtraction result:", sub_result, "\n")
cat("Multiplication result:", mul_result, "\n")
